package schema

import (
	"entgo.io/ent"
	"entgo.io/ent/schema/edge"
	"entgo.io/ent/schema/field"
	"time"
)

type Contact struct {
	ent.Schema
}

func (Contact) Fields() []ent.Field {
	return []ent.Field{
		field.String("first_name"),
		field.String("last_name"),
		field.String("middle_name").Optional(),

		field.String("phone").Optional(),
		field.String("secondary_phone").Optional(),

		field.String("email").Optional(),

		field.Enum("gender").Values("male", "female", "other").Optional(),

		field.Time("date_of_birth").Optional(),

		field.Enum("marital_status").Values(
			"single",
			"married",
			"divorced",
			"widowed",
		).Optional(),

		field.String("occupation").Optional(),

		field.String("address_line1").Optional(),
		field.String("address_line2").Optional(),
		field.String("city").Optional(),
		field.String("state").Optional(),
		field.String("country").Optional(),
		field.String("postal_code").Optional(),

		field.String("emergency_contact_name").Optional(),
		field.String("emergency_contact_phone").Optional(),
		field.String("emergency_contact_relationship").Optional(),

		field.String("profile_picture_url").Optional(),

		// Identity & church records
		field.String("id_number").Optional(),
		field.String("hometown").Optional(),
		field.String("region").Optional(),
		field.String("sunday_school_class").Optional(),
		field.Enum("day_born").Values(
			"monday", "tuesday", "wednesday", "thursday",
			"friday", "saturday", "sunday",
		).Optional(),
		field.Int("membership_year").Optional(),

		// Spouse
		field.Bool("has_spouse").Default(false),
		field.Int("spouse_id").Optional(),

		// Church membership (for congregation members without a system account)
		field.Int("church_id").Optional(),

		// Baptism
		field.Bool("is_baptized").Default(false),
		field.String("baptized_by").Optional(),
		field.String("baptism_church").Optional(),
		field.String("baptism_cert_number").Optional(),
		field.Time("baptism_date").Optional(),

		field.Time("created_at").Default(time.Now),
		field.Time("updated_at").Default(time.Now).UpdateDefault(time.Now),
	}
}

func (Contact) Edges() []ent.Edge {
	return []ent.Edge{
		edge.To("user", User.Type).
			Unique(),

		edge.From("church", Church.Type).
			Ref("contacts").
			Field("church_id").
			Unique(),

		// Self-referential spouse relationship.
		// Named "spouse_contact" / "spouse_of_contact" to avoid clashing with
		// the boolean "has_spouse" field predicate generated by ent.
		edge.To("spouse_contact", Contact.Type).
			Field("spouse_id").
			Unique(),
		edge.From("spouse_of_contact", Contact.Type).
			Ref("spouse_contact").
			Unique(),
		edge.To("attendances", Attendance.Type),
		edge.From("groups", Group.Type).
			Ref("members"),
		edge.From("leading_groups", Group.Type).
			Ref("leader"),
		edge.From("giving_records", Finance.Type).
			Ref("donor"),
		edge.To("pledges", Pledge.Type),
		edge.To("roster_entries", RosterEntry.Type),
		edge.To("prayer_requests", PrayerRequest.Type),
		edge.To("pastoral_notes", PastoralNote.Type),
	}
}
